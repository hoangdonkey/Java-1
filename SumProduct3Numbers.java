public class SumProduct3Numbers {
    public static void main(String[] args) {
        int num1 = 98;
        int num2 = 5;
        int num3 = 77;
        int sum, pro;
        sum = num1+num2+num3;
        pro= num1*num2*num3;

        System.out.println("98 + 5 + 77 = "+sum);
        System.out.println("98 * 5 * 77 = "+pro);
    }
}
